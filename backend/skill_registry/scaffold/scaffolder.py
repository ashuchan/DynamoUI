"""
Scaffolder — generates *.skill.yaml content from a live PostgreSQL table.
Uses schema_inspector lazily to avoid mandatory DB dependency at import time.
"""
from __future__ import annotations

import textwrap
from pathlib import Path
from typing import Any

import structlog
import yaml

log = structlog.get_logger(__name__)

SCAFFOLD_HEADER = "# Auto-generated by dynamoui scaffold. Review all TODO fields before committing.\n"


def _type_hint_for_pg_type(pg_type: str) -> str:
    """Map a PostgreSQL column type string to a DynamoUI skill field type."""
    pg_type = pg_type.lower()
    if "uuid" in pg_type:
        return "uuid"
    if any(t in pg_type for t in ("int", "serial", "bigint", "smallint")):
        return "integer"
    if any(t in pg_type for t in ("float", "real", "double", "numeric", "decimal")):
        return "float"
    if "bool" in pg_type:
        return "boolean"
    if any(t in pg_type for t in ("date", "timestamp", "time")):
        return "date"
    if "json" in pg_type:
        return "json"
    return "string"


def scaffold_from_columns(
    table: str,
    schema: str,
    adapter_key: str,
    columns: list[dict[str, Any]],
) -> str:
    """
    Build a *.skill.yaml string from a list of column descriptors.

    Each column dict must have:
      - name: str
      - type: str (PostgreSQL type)
      - nullable: bool
      - is_pk: bool
    """
    entity = "".join(word.capitalize() for word in table.split("_"))

    fields: list[dict] = []
    for col in columns:
        field_type = _type_hint_for_pg_type(col["type"])
        field_def: dict = {
            "name": col["name"],
            "type": field_type,
            "nullable": col.get("nullable", True),
            "isPK": col.get("is_pk", False),
        }
        if col.get("is_pk"):
            field_def["nullable"] = False
        if field_type == "enum":
            field_def["enumRef"] = "# TODO: set enum name"
        fields.append(field_def)

    skill_data: dict = {
        "entity": entity,
        "table": table,
        "adapter": adapter_key,
        "description": f"# TODO: describe {entity}",
        "schema_name": schema,
        "fields": fields,
        "display": {
            "default_sort_field": fields[0]["name"] if fields else "",
            "default_sort_dir": "asc",
            "columns_visible": [f["name"] for f in fields[:8]],
            "detail_fields": [f["name"] for f in fields],
            "searchable_fields": [],
            "page_size": 25,
        },
        "mutations_file": f"# TODO: set path or leave empty for read-only",
        "patterns_file": f"# TODO: set path",
        "read_permissions": [],
        "write_permissions": [],
    }

    output = SCAFFOLD_HEADER
    output += yaml.dump(skill_data, default_flow_style=False, sort_keys=False, allow_unicode=True)
    log.info("scaffolder.generated", entity=entity, table=table, fields=len(fields))
    return output


async def scaffold_table(
    adapter_key: str,
    table_name: str,
    schema_name: str = "public",
) -> str:
    """
    Inspect a live PostgreSQL table and return a skill YAML string.
    Requires an active adapter connection.
    """
    from backend.adapters.postgresql.schema_inspector import SchemaInspector

    inspector = SchemaInspector(adapter_key)
    columns = await inspector.inspect_table(table_name, schema_name)
    return scaffold_from_columns(table_name, schema_name, adapter_key, columns)


async def scaffold_schema(
    adapter_key: str,
    schema_name: str = "public",
    output_dir: Path | None = None,
) -> dict[str, str]:
    """
    Inspect all tables in a PostgreSQL schema and return {table_name: yaml_content}.
    If output_dir is provided, writes files to disk.
    """
    from backend.adapters.postgresql.schema_inspector import SchemaInspector

    inspector = SchemaInspector(adapter_key)
    tables = await inspector.list_tables(schema_name)
    results: dict[str, str] = {}

    for table in tables:
        yaml_content = await scaffold_table(adapter_key, table, schema_name)
        results[table] = yaml_content

        if output_dir is not None:
            out_path = output_dir / f"{table}.skill.yaml"
            out_path.write_text(yaml_content, encoding="utf-8")
            log.info("scaffolder.written", path=str(out_path))

    return results
